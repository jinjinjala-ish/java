package variable;

public class Var4 {
    public static void main(String[] args){
        //정수
        byte b = 127; //-128 ~ 127
        short s = 32767; //-32,768 ~ 32,767
        int i = 2147483647; //21억 -2,147,483,648 ~ 2,147,483,647
        long l = 9223372036854774807L; // 뒤에 L을 써줘야 함

        //실수
        float f =10.0f; //뒤에 f를 써줘야 함
        double d = 10.0;

    }
}
